import time

class Solution(object):
    def largestPerimeter(self, A):
        """
        :type A: List[int]
        :rtype: int
        """        
        res = 0
        n = len(A)
        for a in range(n):
            for b in [x for x in range(n) if x not in [a]]:
                for c in [x for x in range(n) if x not in [a, b]]:
                    tmp = [A[x] for x in [a,b,c]]
                    residue = max(tmp) - min(tmp)
                    if all([x>residue for x in tmp]): 
                        s_tmp = tmp[0] + tmp[1] + tmp[2]
                        if res>s_tmp: res = res
                        else: res = s_tmp
        
        return res


